#!/usr/bin/env node
'use strict';
/**
 * A class representing a rectangle.
 */
class Rectangle {
  /**
   * Creates a new Rectangle instance.
   * @param {number} w - The width of the rectangle.
   * @param {number} h - The height of the rectangle.
   */
  constructor(w, h) {
    this.width = w;
    this.height = h;
  }
}

export default Rectangle;
